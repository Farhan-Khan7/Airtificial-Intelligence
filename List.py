colors = ["Red", "Green", "Blue", "Yellow", "Green"]
#          [0]      [1]     [2]      [3]      [4]

print(colors[0])  # Output: Red
print(colors[2])  # Output: Blue
print(colors[4])  # Output: Green
print(colors[-1]) # Output: Green
print(colors[-3]) # Output: Blue
print(colors[-5]) # Output: Red
print(colors[1:4]) # Output: ['Green', 'Blue', 'Yellow']
print(colors[:3])  # Output: ['Red', 'Green', 'Blue']
print(colors[2:])  # Output: ['Blue', 'Yellow', 'Green']
print(colors[-4:-1]) # Output: ['Green', 'Blue', 'Yellow']
print(colors[-3:])   # Output: ['Blue', 'Yellow', 'Green']  
print(colors[:-2])  # Output: ['Red', 'Green', 'Blue']
print(colors[:])    # Output: ['Red', 'Green', 'Blue', 'Yellow', 'Green']